import { stripDiacritics } from '../normalization/diacritics.js'

const URDU_ALPHABET: readonly number[] = [
  0x0621, 0x0627, 0x0628, 0x067e, 0x062a, 0x0679, 0x062b,
  0x062c, 0x0686, 0x062d, 0x062e, 0x062f, 0x0688, 0x0630,
  0x0631, 0x0691, 0x0632, 0x0698, 0x0633, 0x0634, 0x0635,
  0x0636, 0x0637, 0x0638, 0x0639, 0x063a, 0x0641, 0x0642,
  0x06a9, 0x06af, 0x0644, 0x0645, 0x0646, 0x06ba, 0x0648,
  0x06c1, 0x06be, 0x06cc, 0x06d2,
]

export const SORT_ORDER_MAP: ReadonlyMap<number, number> = new Map(
  URDU_ALPHABET.map((cp, i) => [cp, i + 1])
)

export function sortKey(word: string): string {
  const clean = stripDiacritics(word)
  return [...clean]
    .map(ch => (SORT_ORDER_MAP.get(ch.codePointAt(0) ?? 0) ?? 40).toString().padStart(2, '0'))
    .join('')
}
